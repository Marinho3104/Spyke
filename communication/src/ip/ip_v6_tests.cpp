
#include "ip_v6.h"
#include <cassert>


int main( int argc, char *argv[] ) {
 
  const communication::Ip_V6 ip;
  assert( ! ip.is_valid() );

  const communication::Ip_V6 ip1( { 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12 }, 8000 );
  const communication::Ip_V6 ip2( { 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12 }, 8000 );
  assert( ip1.is_valid() && ip2.is_valid() );
  assert( ip1 == ip2 );

  const communication::Ip_V6 ip3( { 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12 }, 8000 );
  const communication::Ip_V6 ip4( { 0x11, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12, 0x12 }, 8001 );
  assert( ip3.is_valid() && ip4.is_valid() );
  assert( ip3 != ip4 );

  return 0;

}
